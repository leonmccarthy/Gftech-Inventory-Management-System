    private void jButton_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_insertActionPerformed
        if(jTextField_customer_id.getText().isEmpty()||jTextField_customer_name.getText().isEmpty()
                ||jTextField_customer_phoneno.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Cannot store empty field!!!");
        }else
        try {
            Class.forName("org.sqlite.JDBC");
            Connection con = DriverManager.getConnection("jdbc:sqlite:gftech_db.db");
            Statement stmt = con.createStatement();
            String query = "insert into gftech_customer (ID, Name, Phone) values ('"+jTextField_customer_id.getText()+"', "
                    + "'"+jTextField_customer_name.getText()+"', '"+jTextField_customer_phoneno.getText()+"')";

            stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "Customer Successfully inserted!!!");
            load();
            stmt.close();
            con.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error!!! "+ex);
        }
    }//GEN-LAST:event_jButton_insertActionPerformed

