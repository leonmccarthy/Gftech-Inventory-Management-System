    private void jTable_customersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_customersMouseClicked
        DefaultTableModel model = (DefaultTableModel)jTable_customers.getModel();
        int index = jTable_customers.getSelectedRow();
        jTextField_customer_id.setText(model.getValueAt(index, 0).toString());
        jTextField_customer_name.setText(model.getValueAt(index, 1).toString());
        jTextField_customer_phoneno.setText(model.getValueAt(index, 2).toString());
        try{
            Class.forName("org.sqlite.JDBC");
            Connection con = DriverManager.getConnection("jdbc:sqlite:gftech_db.db");
            Statement stmt = con.createStatement();
            Statement stmt1 = con.createStatement();
            ResultSet set = stmt.executeQuery("select OrderID from gftech_order where CustName = '"+model.getValueAt(index, 1).toString()+"'");
            ResultSet set1 = stmt1.executeQuery("select Amount from gftech_order where CustName = '"+model.getValueAt(index, 1).toString()+"'");
            while(set.next()){
                jLabel_orderno.setText(""+set.getInt(1));
            }
            while(set1.next()){
                jLabel_orderamount.setText("Sh. "+set1.getInt(1));
            }
            stmt.close();
            con.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error!!!"+e);
        }
    }//GEN-LAST:event_jTable_customersMouseClicked

