    private void jButton_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_searchActionPerformed
        try {
            Class.forName("org.sqlite.JDBC");
            Connection con = DriverManager.getConnection("jdbc:sqlite:gftech_db.db");
            //            Statement stmt = con.createStatement();
            String query = "select * from gftech_category where Name = '"+jTextField_categoty_name.getText()+"' ";
            PreparedStatement stmt = con.prepareStatement(query);
            //            stmt.executeUpdate(query);
            ResultSet set = stmt.executeQuery();
            while(set.next()){
                jTextField_categoty_name.setText(set.getString("Name"));
                jTextField_category_id.setText(set.getString("ID"));
                }
            stmt.close();
            con.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error!!! "+ex);
        }
    }//GEN-LAST:event_jButton_searchActionPerformed

