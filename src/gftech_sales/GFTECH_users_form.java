    private void jButton_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_insertActionPerformed
        if(jTextField_user_id.getText().isEmpty()||jTextField_user_name.getText().isEmpty()||
                jTextField_user_password.getText().isEmpty()||jTextField_user_phoneno.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Cannot store empty field!!!");
        }else
        try {
            Class.forName("org.sqlite.JDBC");
            Connection con = DriverManager.getConnection("jdbc:sqlite:gftech_db.db");
            Statement stmt = con.createStatement();
            String query = "insert into gftech_user (ID, Name, Password,  Phone) values ('"+jTextField_user_id.getText()+"',"
                    + "'"+jTextField_user_name.getText()+"', '"+jTextField_user_password.getText()+"', "
                    + "'"+jTextField_user_phoneno.getText()+"')";
            stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "User Successfully inserted!!!");
            load();
            stmt.close();
            con.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error!!! "+ex);
        }
    }//GEN-LAST:event_jButton_insertActionPerformed

